# A program that, for a given speed in km/h,
# prints the speed in m/s
#
speed_kmh = 70
speed_ms = speed_kmh*5/18 
print(speed_kmh, "km/h = ", speed_ms, "m/s")

print(speed_kmh, "km/h =", round(speed_ms, 2), "m/s")


